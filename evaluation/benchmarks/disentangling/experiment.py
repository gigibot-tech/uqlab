"""Lightweight adapter: reads results.pt from completed experiments.

Architecture:
1. User runs experiments via run_fast_uncertainty_classification.py
2. Pipeline generates results.pt with signal_table
3. This adapter reads results.pt and extracts signals
4. Vendor library gets (classifications, aleatorics, epistemics)

Signal mapping (paper default — Information-theoretic / MC dropout during the job)::

- aleatorics = signal_table['expected_entropy']
- epistemics = signal_table['mutual_info']

Signal preset (``predict_mode="signal"`` → ``signal_dualxda`` — DualXDA during the job)::

- aleatorics = signal_table['inverse_coherence_dualxda']  (alias: inverse_coherence)
- epistemics = signal_table['inverse_mass_dualxda']  (alias: inverse_mass)

EK-FAC preset (``predict_mode="signal_ek_fak"``)::

- aleatorics = signal_table['inverse_coherence_ek_fak']
- epistemics = signal_table['inverse_mass_ek_fak']

``predict_disentangling`` only reads ``results.pt``; see ``uqlab.shared.config.signals``.

The vendored disentanglement_error library expects:
"""

from __future__ import annotations

from pathlib import Path
from typing import Any, Tuple

import numpy as np

from uqlab.evaluation.artifacts import EvalRunArtifacts
from uqlab.evaluation.signals.catalog import (
    METRIC_META,
    aleatoric_signal_ids,
    epistemic_signal_ids,
)

SIGNAL_REGISTRY = METRIC_META
from uqlab.shared.config.signals import (
    PREDICT_DISENTANGLING_NOTE,
    bridge_job_requirements,
    resolve_disentangling_signal_pair,
)
from uqlab.vendor.disentanglement_error.disentangling_model import DisentanglingModel

__all__ = [
    "ExperimentDisentanglingModel",
    "UQLabDisentanglingBridge",
    "resolve_disentangling_signal_pair",
]


class ExperimentDisentanglingModel(DisentanglingModel):
    """
    Results reader for disentanglement benchmarks.
    
    This is NOT a trainable model - it's an adapter that reads pre-computed
    uncertainty signals from results.pt files generated by the main pipeline.
    
    The disentanglement benchmark sweeps over label_noise and dataset_size,
    running experiments separately. This adapter simply reads the results.
    
    Usage:
        # Simple results reader (experiments already run)
        model = ExperimentDisentanglingModel(results_dir="./results/run_0001")
        predictions, aleatorics, epistemics = model.predict_disentangling(x_test)
        
        # Legacy benchmark mode (for backward compatibility)
        model = ExperimentDisentanglingModel.from_workflow_defaults()
        model.fit(x_train, y_train, label_noise=0.1)  # Runs pipeline
        predictions, aleatorics, epistemics = model.predict_disentangling(x_test)
    """

    def __init__(
        self,
        results_dir: Path | str | None = None,
        *,
        predict_mode: str = "paper",
        aleatoric_signal: str | None = None,
        epistemic_signal: str | None = None,
        workflow: dict[str, Any] | None = None,
    ) -> None:
        """
        Initialize results reader.

        Args:
            results_dir: Directory containing results.pt
            predict_mode: ``"paper"``, ``"signal"`` (alias → DualXDA), ``"signal_dualxda"``,
                or ``"signal_ek_fak"``
            aleatoric_signal: Override aleatoric column (beats predict_mode / workflow)
            epistemic_signal: Override epistemic column
            workflow: Optional Streamlit workflow dict (``uncertainty_config`` pairing)
        """
        self.results_dir = Path(results_dir) if results_dir else None
        alea, epi = resolve_disentangling_signal_pair(
            predict_mode=predict_mode,
            aleatoric_signal=aleatoric_signal,
            epistemic_signal=epistemic_signal,
            workflow=workflow,
        )
        self.aleatoric_signal = alea
        self.epistemic_signal = epi
        self._bridge_requirements = bridge_job_requirements(alea, epi)
        self._validate_signals()
        self.results: EvalRunArtifacts | None = None

    def _validate_signals(self) -> None:
        """Validate that requested signals exist in SIGNAL_REGISTRY."""
        if self.aleatoric_signal not in SIGNAL_REGISTRY:
            available = ", ".join(SIGNAL_REGISTRY.keys())
            raise ValueError(
                f"Unknown aleatoric signal: {self.aleatoric_signal!r}. "
                f"Available: {available}"
            )
        if self.epistemic_signal not in SIGNAL_REGISTRY:
            available = ", ".join(SIGNAL_REGISTRY.keys())
            raise ValueError(
                f"Unknown epistemic signal: {self.epistemic_signal!r}. "
                f"Available: {available}"
            )
        
        # Validate signal types match their roles
        alea_ids = aleatoric_signal_ids()
        epi_ids = epistemic_signal_ids()
        
        if self.aleatoric_signal not in alea_ids:
            raise ValueError(
                f"Signal {self.aleatoric_signal!r} is not marked as aleatoric. "
                f"Aleatoric signals: {', '.join(alea_ids)}"
            )
        if self.epistemic_signal not in epi_ids:
            raise ValueError(
                f"Signal {self.epistemic_signal!r} is not marked as epistemic. "
                f"Epistemic signals: {', '.join(epi_ids)}"
            )

    @classmethod
    def from_workflow_defaults(cls, **kwargs: Any) -> "ExperimentDisentanglingModel":
        """
        Legacy constructor for backward compatibility with benchmark runners.
        
        Note: This creates an instance without a results_dir, which means
        fit() must be called to run experiments before predict_disentangling().
        """
        return cls(results_dir=None, **kwargs)

    def fit(self, x_train, y_train, **ctx: Any) -> None:
        """
        No-op: experiments already run by main pipeline.
        
        The disentanglement benchmark calls this method with label_noise and
        dataset_size parameters, but we expect experiments to be run separately.
        
        For legacy benchmark mode (from_workflow_defaults), this would trigger
        pipeline.run(), but that functionality has been removed per the
        simplification requirements.
        """
        if ctx:
            raise NotImplementedError(
                "Legacy benchmark mode (running experiments from fit()) has been removed. "
                "Run experiments separately via ``uqlab.runner.pipeline.run`` or the CLI, "
                "then create ExperimentDisentanglingModel with results_dir pointing to the output."
            )

    def predict_disentangling(self, x_test) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
        """
        Return ``(predictions, aleatorics, epistemics)`` from ``results.pt``.

        Does **not** run MC dropout or DualXDA. Those run during ``run_experiment_core``
        when the job enabled the sources required by the bridge pair
        (see ``bridge_job_requirements`` / ``PREDICT_DISENTANGLING_NOTE`` in
        ``uqlab.shared.config.signals``).

        Args:
            x_test: Ignored (results already computed for the fixed eval set)
        """
        if self.results_dir is None:
            raise RuntimeError(
                "results_dir not set. Create instance with results_dir parameter, "
                "or use from_workflow_defaults() and call fit() first."
            )
        
        results_path = self.results_dir / "results.pt"
        
        if not results_path.exists():
            raise FileNotFoundError(
                f"results.pt not found in {self.results_dir}. "
                "Run experiments first via ``uqlab.runner.pipeline.run`` or the CLI"
            )
        
        # Load results and extract signals using registry
        self.results = EvalRunArtifacts.from_results_pt(results_path)
        
        return self.results.disentangling_vectors(
            aleatoric_signal=self.aleatoric_signal,
            epistemic_signal=self.epistemic_signal,
        )


# Backward compatibility alias
UQLabDisentanglingBridge = ExperimentDisentanglingModel

